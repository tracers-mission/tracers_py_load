class EADload(getTime):
    """
    This class loads and reads ephemeris and attitude information from EAD files generated by SOC.
    
    Required inputs
    -------------------
    t0 = start time of observation (string in datetime format. i.e., '2025-11-06/12:00:34')
    tf = end time of observation in same format as t0
    spacecraft = '1' or '2' for whichever TRACERS satellite you're aiming to look at.
    """
    
    def __init__(self,t0,tf,spacecraft):
        super().__init__(t0=t0,tf=tf,spacecraft=spacecraft)
        self.spacecraft=spacecraft
    def read_data(self,params=None,local_dir=None,quiet=None,username=None,password=None):
        """
        Optional inputs:
        params: list of named parameters to load from EAD file
        local_dir: local path to store / search for EAD files
        quiet: default False. if True, will suppress printing file name that is being loaded
        username & password: required inputs if you wish to pull from team portal
        ** 2026-04-15: currently, EAD files are only available on the team portal, not public facing! **
        """
        if quiet is None:
            quiet = False
        files2load = []
        for d in range(len(self.date_list)):
            ds = self.date_list[d]
            year = ds.split('/')[0]
            month = ds.split('/')[1]
            day = ds.split('/')[2]
            date_string = year+month+day
            cwd = os.getcwd()
            
            if local_dir is None:
                cwd = os.getcwd()
                local_dir = f'{cwd}/data/TS{self.spacecraft}/EAD/{year}/{month}/'
                if os.path.exists(f'{cwd}/data/') is False:
                    os.mkdir(f'{cwd}/data/')
                if os.path.exists(f'{cwd}/data/TS{self.spacecraft}/') is False:
                    os.mkdir(f'{cwd}/data/TS{self.spacecraft}/')
                if os.path.exists(f'{cwd}/data/TS{self.spacecraft}/EAD/') is False:
                    os.mkdir(f'{cwd}/data/TS{self.spacecraft}/EAD/')   
                if os.path.exists(f'{cwd}/data/TS{self.spacecraft}/EAD/{year}') is False:
                    os.mkdir(f'{cwd}/data/TS{self.spacecraft}/EAD/{year}')
                if os.path.exists(f'{cwd}/data/TS{self.spacecraft}/EAD/{year}/{month}') is False:
                    os.mkdir(f'{cwd}/data/TS{self.spacecraft}/EAD/{year}/{month}') 
                date_dir = f'{cwd}/data/TS{self.spacecraft}/EAD/{year}/{month}'

            else:
                date_dir = local_dir

            f2find = f'{date_dir}/ts{self.spacecraft}_def_ead**{date_string}**.cdf'
            out = os.popen(f'ls -rt {f2find}').read()
            ead_local_files = out.split('\n')[0:-1]
            
            if len(ead_local_files) > 0:
                files2load.append(ead_local_files[-1])
            else:
                ead_download = retrieve_ead_data(year,month,day,self.spacecraft, local_dir=local_dir, \
                                                 username=username, password=password)
                f2find = f'{date_dir}/ts{self.spacecraft}**_ead_**{date_string}**.cdf'
                out = os.popen(f'ls -rt {f2find}').read()
                ead_local_files = out.split('\n')[0:-1]
                if len(ead_local_files) > 0:
                    files2load.append(ead_local_files[-1])

        self.filenames = files2load
        if len(files2load) > 0:
            ead_dict = read_ead_files(files2load, start=self.start, end=self.end, params=params, quiet=quiet)
        else:
            ead_dict = {}
            print('No EAD files to load!')
        return ead_dict 

