def read_ead_files(files2load, start=None, end=None, params=None, quiet=None):
    """
    This function reads in EAD files. Required input is a list of file names.
    Optional input includes a start time (string), end time (string), and list of parameters to load.
    Default is loading the entire list of parameters. Valid parameter codes to request are as follows:
    
    'r_gei','v_gei','r_geo','lat_geo','lon_geo','r_geo','lat_geod','lon_geod','alt_geod','r_sm',
    'mlat','mlt','orbit','ltdn','b_gei','spin_lat','spin_lon','spin_phase'    
    """
    if quiet is None:
        quiet = False
        
    sc = (files2load[0].split('/')[-1]).split('_')[0]
    
    param_list = [f'{sc}_ead_r_gei2000',f'{sc}_ead_v_gei2000',f'{sc}_ead_r_geo',f'{sc}_ead_lat_geo',\
                   f'{sc}_ead_lon_geo',f'{sc}_ead_radius_geo',f'{sc}_ead_lat_geod',f'{sc}_ead_lon_geod',\
                   f'{sc}_ead_altitude_geod',f'{sc}_ead_r_sm',f'{sc}_ead_mlat',f'{sc}_ead_mlt',\
                   f'{sc}_ead_orbit_num',f'{sc}_ead_ltdn',f'{sc}_ead_b_gei2000',f'{sc}_ead_spin_axis_lat_gei2000',\
                   f'{sc}_ead_spin_axis_lon_gei2000',f'{sc}_ead_spin_phase1']
    param_codes = ['r_gei','v_gei','r_geo','lat_geo','lon_geo','r_geo','lat_geod','lon_geod','alt_geod','r_sm',\
                   'mlat','mlt','orbit','ltdn','b_gei','spin_lat','spin_lon','spin_phase']

    threeD_params = [f'{sc}_ead_b_gei2000',f'{sc}_ead_r_sm',f'{sc}_ead_r_geo',f'{sc}_ead_r_gei2000',f'{sc}_ead_v_gei2000']
                       
    if params is not None:
        overlap_idx = find_instances(params,param_codes)
        param_list = [param_list[x] for x in overlap_idx]
                    
    data_dict = {'dt':[],'utc':[]}
    for key in param_list:
        data_dict[key] = []
    param_shape = []                       
    for q in range(len(files2load)):
        file = files2load[q]
        cdf_file = cdflib.CDF(file)
        
        if quiet is False:
            print(file)            
        # Converting from nonsensical time to UTC.
        # These are the times at the midpoint of each energy sweep.
        dt_from_cdf = cdflib.cdfepoch.to_datetime(cdf_file['Epoch'])
        dts = pd.to_datetime(dt_from_cdf)
        utcs = [(x - pd.Timestamp('1970-01-01'))/pd.Timedelta('1s') for x in dts]

        data_dict['dt'].append(dts)
        data_dict['utc'].append(utcs)

        for j in range(len(param_list)):
            param2load = param_list[j]
            var_dat = cdf_file[param2load]
            data_dict[param2load].append(var_dat)
            if q == 0:
                param_shape.append(np.shape(var_dat))
    
    # ;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
    # RESHAPING ARRAYS BASED ON MULTIPLE FILES   
    n_files = len(files2load)
    t_tot = 0
    t_shapes = []
    for i in range(n_files):
        d2 = np.shape(data_dict['dt'][i])[0]
        t_tot = t_tot + int(d2)
        t_shapes.append(d2)

    reshape_dict = {'times':np.zeros(t_tot),'dt':np.zeros(t_tot)}
    for p in range(len(param_list)):
        if param_list[p] in threeD_params:
            reshape_dict[param_list[p]] = np.zeros((t_tot, 3))
        else:
            reshape_dict[param_list[p]] = np.zeros((t_tot))        

    
    for k in range(n_files):
        if k == 0:
            indices = [0,t_shapes[k]]
        elif k != 0 and k < n_files - 1:
            prior_indices = np.sum(t_shapes[0:k])
            indices = [prior_indices,prior_indices+t_shapes[k]]
        else:
            prior_indices = np.sum(t_shapes[0:k])
            indices = [prior_indices,t_tot]
        reshape_dict['times'][indices[0]:indices[1]] = data_dict['utc'][k]
        reshape_dict['dt'][indices[0]:indices[1]] = data_dict['dt'][k]

        for param in param_list:
            if param in threeD_params:
                reshape_dict[param][indices[0]:indices[1],:] = data_dict[param][k]
            else:
                reshape_dict[param][indices[0]:indices[1]] = data_dict[param][k]
            
    times = reshape_dict['times']
    nan_idx = list(np.where(np.isfinite(times) == False)[0])

    if len(nan_idx) > 0:
        for idx in nan_idx:
            if idx == 0 :
                ii = [idx+1, idx+2]
                time_res = abs(times[ii[1]] - times[ii[0]])
                new_time = times[ii[0]] - time_res
            elif idx != len(data_dict['utc']) - 1:
                ii = [idx-1,idx+2]
                new_time = (times[ii[0]] + times[ii[1]])/2.
            else:
                ii = [idx-2, idx-1]
                time_res = abs(times[ii[1]] - times[ii[0]])
                new_time = times[ii[1]] + time_res                
            times[idx] = new_time 

    if start is None and end is None:
        f_time = (files2load[0].split('v')[0]).split('_')[-2]
        start = dt.datetime.strptime(f_time,'%Y%m%d')

        f_time2 = (files2load[-1].split('v')[0]).split('_')[-2]
        s2 = dt.datetime.strptime(f_time2,'%Y%m%d')
        end = s2 + dt.timedelta(days=1)
    
    if isinstance(start,str):
        dt_fmt = determine_datetime_type(start)
        start = dt.datetime.strptime(start, dt_fmt)
        end = dt.datetime.strptime(end, dt_fmt)

    
    start_time = (start - dt.datetime(1970,1,1)).total_seconds()
    end_time = (end - dt.datetime(1970,1,1)).total_seconds()
    time_mask = (times >= start_time)&(times <= end_time) 


    out_dict = {'UTC':times[time_mask]}
    for param in param_list: 
        if param in threeD_params:
            out_dict[param] = reshape_dict[param][time_mask,:]
        else:
            out_dict[param] = reshape_dict[param][time_mask]

    out_dict['DT'] = []
    for elem in out_dict['UTC']:
        out_dict['DT'].append(dt.datetime.fromtimestamp(elem,dt.UTC))
 
    return out_dict




    
